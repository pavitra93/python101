# Round function

# returns float
interest = 3 / 2;
print(interest)

# rounds to a integer or float upto decimals
interest = 100 / 3;
print(round(interest))
print(round(interest, 2))

# returns integer after division
interest = 100 // 3;
print(round(interest))

# increment, decrement, mul & divide 
point = 0
point += 1 
print(point)